public class Boss {
    public int getAttack() {
        return attack;
    }

    public void setAttack(int attack) {
        this.attack = attack;
    }

    public int getHeals() {
        return Heals;
    }

    public void setHeals(int heals) {
        Heals = heals;
    }

    public String getDefense() {
        return Defense;
    }

    public void setDefense(String defense) {
        Defense = defense;
    }

    private int attack;
    private int Heals;
    private String Defense;
}

